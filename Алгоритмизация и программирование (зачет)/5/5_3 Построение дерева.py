# Требующийся функционал программы:
# 1. Создать  программу, реализующую поиск, вставку и удаление элементов в дереве и в красно-чёрном дереве.
# 2. Полноценный интерфейс, позволяющий вводить числовые и символьные значения, обработка отрицательных значений, ошибка при вводе текста. Интерфейс также должен обладать возможностью отображать ветви дерева. Для красно-чёрного дерева должна проводится отрисовка до и после поворота.

